package exercise4;

public class Tutorial15 {
    public static void main(String[] args) {
        int a,b;
        a=100;
        b=200;
        System.out.println("Before Swap");
        System.out.println("x = " +a);
        System.out.println("y = " +b);

        int c= a;
        a=b;
        b=c;
        System.out.println("After Swap");
        System.out.println("x = " +a);
        System.out.println("y = " +b);





    }
}
